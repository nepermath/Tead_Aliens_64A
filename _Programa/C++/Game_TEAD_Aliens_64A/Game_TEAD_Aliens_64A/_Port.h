#ifndef _PORT_H_
#define _PORT_H_

/*
 +--------------------------------------+
 |        HARDWARE DEPENDENT INCLUDES	|
 +--------------------------------------+
*/
#include <avr/io.h>

/*
 +--------------------------------------+
 |        C LIBRARY INCLUDES		    |
 +--------------------------------------+
*/
#include <stdint.h>
/*
 +--------------------------------------+
 |        TYPEDEF DEFINITIONS		    |
 +--------------------------------------+
*/

typedef volatile uint8_t * PORT;

/*
    +--------------------------------------+
    |        PUBLIC FUNCTIONS		       |
    +--------------------------------------+
*/

/************************************************************************/
/* Write Value into a 8 bit port                                        */
/************************************************************************/
inline void	portWrite(PORT port,uint8_t value)
{
	*port=value;
}
/************************************************************************/
/* Read a Value from 8 bit port											*/
/************************************************************************/
inline uint8_t portRead(PORT port)
{
	return *port;
}
/************************************************************************/
/* Read a bit  value from port, output 0 or 1                           */
/************************************************************************/
inline uint8_t portReadBit(PORT port,uint8_t bit)
{
	return *port&(1<<bit);
}
/************************************************************************/
/* Set Bit into a 8 bit Port                                            */
/************************************************************************/
inline void	portOrBit(PORT port,uint8_t bit)
{
	*port|=(1<<bit);
}
/************************************************************************/
/* Clear Bit into a 8 bit Port                                          */
/************************************************************************/
inline void	portAndBit(PORT port,uint8_t bit)
{
	*port&=~(1<<bit);
}
/************************************************************************/
/* Invert Bit Into a 8 bit Port                                         */
/************************************************************************/
inline void	portXorBit(PORT port,uint8_t bit)
{
	*port^=(1<<bit);
}
/************************************************************************/
/* Or Port with value                                                   */
/************************************************************************/
inline void	portOr(PORT port,uint8_t value)
{
	*port|=value;
}
/************************************************************************/
/* And Port with value                                                   */
/************************************************************************/
inline void	portAnd(PORT port,uint8_t value)
{
	*port&=~value;
}
/************************************************************************/
/* Xor Port with value                                                   */
/************************************************************************/
inline void	portXor(PORT port,uint8_t value)
{
	*port^=value;
}

#endif